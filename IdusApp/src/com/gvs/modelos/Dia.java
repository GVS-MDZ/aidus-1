package com.gvs.modelos;

public class Dia {

	private String dia;
	private String dia_completo;

	public Dia(String dia, String dia_completo) {
		this.dia = dia;
		this.dia_completo = dia_completo;
	}

	public String getDia() {
		return dia;
	}

	public void setDia(String dia) {
		this.dia = dia;
	}

	public String getDia_completo() {
		return dia_completo;
	}

	public void setDia_completo(String dia_completo) {
		this.dia_completo = dia_completo;
	}

}
